/*
Марат очень любит футбол. Однажды, глядя футбольный матч, он записывал на листе бумаги текущее положение игроков. Для простоты он изобразил ситуацию в виде строки из нулей и единиц. Ноль соответствует игрокам одной команды, единица — игрокам другой команды. Если есть как минимум 7 игроков некоторой команды, стоящих подряд, то эта ситуация считается опасной. Например, ситуация 00100110111111101 — опасная, а 11110111011101 — нет. Вам задана текущая ситуация. Определите, является ли она опасной.

Входные данные
В первой строке ввода задана непустая строка из символов «0» и «1», обозначающих игроков. Длина строки не превышает 100 символов. От каждой команды на поле присутствует хотя бы один игрок.

Выходные данные
Выведите «YES» если ситуация опасная. В противном случае выведите «NO».

Примеры
входные данные
001001
выходные данные
NO
входные данные
1000000001
выходные данные
YES
*/

import java.util.Scanner;

public class Task96A {

	public static void main (String[] args) {
		Scanner sc = new Scanner(System.in);
		String position = sc.nextLine();
		boolean flag = false;
		int counter = 1;
		position = position + '.';
		for (int i = 0; i < position.length()-1; i++) {
			if (position.charAt(i) == position.charAt(i+1)) {
				counter++;
			} else {
				if (counter >= 7) {
					flag = true;
					break;
				} else {
					counter = 1;
				}
			}
		}
		if (flag) {
			System.out.println("YES");
		} else {
			System.out.println("NO");
		}
	}
}		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
